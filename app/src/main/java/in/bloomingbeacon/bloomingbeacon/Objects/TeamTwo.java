public class TeamTwo {

    private String two_name;
    private String two_post;
    private String two_image;

    public TeamTwo() {
    }

    public TeamTwo(String two_name, String two_post, String two_image) {
        this.two_name = two_name;
        this.two_post = two_post;
        this.two_image = two_image;
    }

    public String getTwo_name() {
        return two_name;
    }

    public String getTwo_post() {
        return two_post;
    }

    public String getTwo_image() {
        return two_image;
    }
}
